function watchMode() {
  console.log('\nüîÑ MODO WATCH ACTIVO - Actualizando cada 5 segundos...\n');
  console.log('Presiona Ctrl+C para salir\n');
  
  let iteration = 0;
  
  const interval = setInterval(() => {
    iteration++;
    console.log(`\n‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ ITERACI√ìN ${iteration} ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ`);
    
    displayDashboard();
    
    const metrics = getMetrics();
    const alerts = getAlerts();
    
    if (alerts.some(a => a.level === 'CRITICAL')) {
      console.log('\nüîî ALERTA CR√çTICA DETECTADA - Revisar inmediatamente!');
    }
  }, 5000);
  
  process.on('SIGINT', () => {
    clearInterval(interval);
    console.log('\n\nüëã Saliendo del modo watch...\n');
    process.exit(0);
  });
}

